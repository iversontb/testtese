<h2>Публичная оферта на продажу туристических услуг.</h2> 
<p>ООО «40-меридиан» именуемое в дальнейшем «Исполнитель», заключает настоящий Договор с любым лицом, именуемым в дальнейшем «Заказчик». Данный Договор является договором предоставления услуг, заключаемым путем публичной оферты, и регламентирует порядок предоставления услуг и обязательства, возникающие в связи с этим между Исполнителем и Заказчиком. Текст настоящего Договора размещен в сети Интернет по адресу http://ilovekolomna.ru.
</p>
<h3>1. Предмет Договора</h3>
<p>1.1. Предметом настоящей оферты является бронирование и продажа Заказчику гостиничных и экскурсионных услуг на условиях данной Оферты и в соответствии с тарифными планами, опубликованными на сайте http://ilovekolomna.ru/
</p> 
<p>1.2. Экскурсионное обслуживание проводится по выбранной Заказчиком программе, в предлагаемые Исполнителем сроки и по заранее определенной цене.
</p>
<h3>2. Права и обязанности сторон</h3> 
<p>2.1. Заказчик вправе. </p>
<p>2.1.1.  Оформлять заказ на бронирование гостиничных и экскурсионных услуг с использованием Интернета, звонка по телефону и прочими способами, указанными на http://ilovekolomna.ru/. При этом Заказчик признает, что в случае использования сервиса Исполнителя он в полной мере и безоговорочно принимает условия данной Оферты в независимости от того, каким способом был совершен заказ.
</p> 
<p>2.1.2. Получить необходимую информацию о выбранной программе, предлагаемых сроках проведения, стоимости обслуживании и включенных в эту стоимость услугах. </p> 
<p>2.1.3. Забронировать выбранную программу, путем передачи заявки по телефону или заполнения заявки на сайте. Фактом принятия заявки является ваучер, который высылается Заказчику по электронной почте.</p> 
<p>2.1.4. СПолучить обслуживание надлежащего качества, а также включенные в стоимость услуги.
</p>
<p>2.1.5. Аннулировать заявку на обслуживании, при соблюдении условии раздела «Порядок расчетов».
</p>
<p>2.2. Исполнитель вправе</p> 
<p>2.2.1. Требовать от Заказчика придерживаться соблюдения всех процедур заказа и бронирования гостиничных услуг строго по правилам, изложенным на сайте http://ilovekolomna.ru/ и в данной Оферте. Какие бы действия не совершал Заказчик, Исполнитель несет ответственность за надлежащее исполнение только действий и процедур, совершенных с полным соблюдением этих правил.
</p> 
<p>2.2.2. Требовать от Заказчика полного согласия с условиями Оферты. Без согласия с условиями оферты отказать Заказчику в оказании услуг.</p> 
<p>2.2.3. Отключать и включать сервис, производить профилактические работы на сервере и другом оборудовании, задействованном в оказании услуг в удобное время, информируя об этом Заказчиков на сайте http://ilovekolomna.ru
</p> 
<p>2.2.4. Назначать даты и маршруты экскурсионного обслуживания, менять количество включенных в стоимость услуг, а также стоимость обслуживания.
</p>
<p>2.2.5. Аннулировать бронирование Заказчиков без объяснения причины. В случае, если на момент аннуляции по инициативе Исполнителя, Заказчик уже оплатил обслуживание частично или полностью, Исполнитель возвращает 100% оплаченных средств.
</p> 
<p>2.2.6.  Поменять порядок предоставления услуг по маршруту, без изменения общего количества услуг.</p>
<p>2.2.7. Уведомлять о новостях, специальных предложениях Исполнителя, а также делать другие уведомления на сайте Исполнителя или посредством электронной почты.</p>
<p>2.3. Заказчик обязан</p>
<p>2.3.1. Согласиться с условиями данной Оферты.</p>
<p>2.3.2. Предоставить достоверную информацию о себе при подаче заявки на обслуживание, а именно: фамилию, имя, отчество, количество человек, адрес электронной почты, номер телефона.</p>
<p>2.3.3. Оплатить выбранную программу предложенными вариантами оплаты в оговоренные при приеме заявки на обслуживания сроки.</p>
<p>2.3.4. Сообщить о невозможности использования экскурсионного обслуживания.</p>
<p>2.3.5. Соблюдать Правила проведения экскурсии.</p>
<p>2.3.6. При наличии претензий по качеству обслуживания или количеству предоставляемых услуг направить письменную претензию в течение 72 часов после окончания обслуживания</p>
<p>2.4. Исполнитель обязан</p>
<p>2.4.1. Предоставить информацию о запланированных датах экскурсионного обслуживания, маршрутах проведения, стоимости и включенных в стоимость услуг. </p>
<p>2.4.2. Принять заявку на обслуживание, и при наличии свободных мест на выбранный маршрут на выбранную дату подтвердить предоставление обслуживания.</p>
<p>2.4.3. Сообщить о вариантах оплаты обслуживания, сроках и условиях аннуляции. </p>
<p>2.4.4. Предоставить экскурсионное обслуживание по выбранному Заказчиком маршруту в выбранную дату с включенными в стоимость услугами. </p>
<p>2.4.5. В случае невозможности проведения обслуживания, заранее сообщить об этом Заказчику.</p>
<p>2.4.6. Принимать необходимые меры для обеспечения безопасности Заказчика.</p>
<h3>3. Порядок расчетов</h3>
<p>3.1. Оплата обслуживания Компании производится по безналичному расчету или за наличный расчет в размере 100% при заселении в отель. Перечень услуг и их стоимость фиксируется при приеме заявки на обслуживание </p> 
<p>3.2. Стоимость обслуживания указана в Российских рублях. Обслуживание не облагается НДС в связи с применением Исполнителем упрощенной системы налогообложения.</p>
<p>3.3. Тарифы и условия распространяются только на время действия программы.</p>
<p>3.4. Цены (тарифы) на обслуживание могут изменяться Исполнителем в одностороннем порядке при условии уведомления об этом Заказчика непосредственно на сайте Исполнителя путем изменения содержания информации о ценах (тарифах), (без специального отдельного объявления). Изменение цен (тарифов) на предоставление обслуживание  не влечет перерасчета стоимости обслуживания, которое было предварительно забронировано Заказчиком до уведомления об изменении цен.</p>
<h3>4. Правила заселения и проживания в отеле.</h3>
<p>4.1. Согласно "Правилам предоставления гостиничных услуг в РФ" от 25.04.1997г. №490, в первую очередь размещение в гостинице предоставляется лицам, забронировавшим номер и подтвердившим данный факт.</p>
<p>4.2. Поселение (подселение) в гостиницу граждан осуществляется по предъявлении:<br>
   - для граждан РФ - паспорта РФ<br>
